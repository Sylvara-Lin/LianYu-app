# AES Inverse S-Box
INV_SBOX = [0] * 256
for i in range(256):
    INV_SBOX[SBOX[i]] = i


def key_expansion_256(key):
    """AES-256 key schedule. Returns 15 round keys (each 16 bytes, round 0 = whitening)."""
    def sub_word(w):
        return ((SBOX[(w >> 24) & 0xFF] << 24) |
                (SBOX[(w >> 16) & 0xFF] << 16) |
                (SBOX[(w >> 8) & 0xFF] << 8) |
                (SBOX[w & 0xFF]))

    def rot_word(w):
        return ((w << 8) | (w >> 24)) & 0xFFFFFFFF

    RCON = [0x01, 0x02, 0x04, 0x08, 0x10, 0x20, 0x40, 0x80, 0x1B, 0x36, 0x6C, 0xD8, 0xAB, 0x4D, 0x9A]

    w = [0] * 60
    for i in range(8):
        w[i] = (key[i*4] << 24) | (key[i*4+1] << 16) | (key[i*4+2] << 8) | key[i*4+3]

    for i in range(8, 60):
        temp = w[i-1]
        if i % 8 == 0:
            temp = sub_word(rot_word(temp)) ^ (RCON[i // 8 - 1] << 24)
        elif i % 8 == 4:
            temp = sub_word(temp)
        w[i] = w[i-8] ^ temp

    round_keys = []
    for r in range(15):
        rk = b''
        for j in range(4):
            rk += w[r * 4 + j].to_bytes(4, 'big')
        round_keys.append(rk)
    return round_keys


def gf_mul(a, b):
    """Multiply in GF(2^8) with AES irreducible polynomial x^8 + x^4 + x^3 + x + 1"""
    p = 0
    for _ in range(8):
        if b & 1:
            p ^= a
        hi = a & 0x80
        a = (a << 1) & 0xFF
        if hi:
            a ^= 0x1B
        b >>= 1
    return p


def mix_columns_column(col):
    """MixColumns transform for 4 bytes (one column). Returns [c0', c1', c2', c3']"""
    return [
        gf_mul(col[0], 2) ^ gf_mul(col[1], 3) ^ col[2] ^ col[3],
        col[0] ^ gf_mul(col[1], 2) ^ gf_mul(col[2], 3) ^ col[3],
        col[0] ^ col[1] ^ gf_mul(col[2], 2) ^ gf_mul(col[3], 3),
        gf_mul(col[0], 3) ^ col[1] ^ col[2] ^ gf_mul(col[3], 2),
    ]


def inv_mix_columns_column(col):
    """InvMixColumns transform for 4 bytes (one column)."""
    return [
        gf_mul(col[0], 14) ^ gf_mul(col[1], 11) ^ gf_mul(col[2], 13) ^ gf_mul(col[3], 9),
        gf_mul(col[0], 9) ^ gf_mul(col[1], 14) ^ gf_mul(col[2], 11) ^ gf_mul(col[3], 13),
        gf_mul(col[0], 13) ^ gf_mul(col[1], 9) ^ gf_mul(col[2], 14) ^ gf_mul(col[3], 11),
        gf_mul(col[0], 11) ^ gf_mul(col[1], 13) ^ gf_mul(col[2], 9) ^ gf_mul(col[3], 14),
    ]


def generate_wb_tables(key_bytes, output_file=sys.stdout):
    """Main table generator — CORRECTED version."""
    import random
    random.seed(0xDEADBEEF)

    if len(key_bytes) != 32:
        raise ValueError("AES-256 requires 32-byte key")

    rks = key_expansion_256(key_bytes)
    out = output_file

    out.write('/* Auto-generated by gen_wb_tables.py — DO NOT EDIT */\n')
    out.write('#include <cstdint>\n\n')

    # ---- External encodings ----
    out.write('/* External input encodings (16 bytes XOR mask) */\n')
    ext_in = [random.randint(0, 255) for _ in range(16)]
    out.write('static const uint8_t g_wb_ext_in[16] = {')
    out.write(','.join(f'0x{b:02x}' for b in ext_in))
    out.write('};\n\n')

    out.write('/* External output encodings (16 bytes XOR mask) */\n')
    ext_out = [random.randint(0, 255) for _ in range(16)]
    out.write('static const uint8_t g_wb_ext_out[16] = {')
    out.write(','.join(f'0x{b:02x}' for b in ext_out))
    out.write('};\n\n')

    # ---- Forward T-Boxes (encrypt) ----
    # T-Box at position pos (row r = pos%4, col c = pos/4):
    #   T(x) = MixColumns_partial(SubBytes(x ^ round_key[pos])) at ShiftRows column
    #   After ShiftRows: byte at (r,c) goes to column (c-r) mod 4
    #   Output: 4 bytes representing this byte's contribution to that column's MixColumns output
    #
    # IMPORTANT: No AddRoundKey here! The round key is embedded INSIDE SubBytes.
    # The C++ encrypt function XORs 4 T-Box outputs per column, which gives
    # the complete MixColumns result for that column.
    # The final round (round 13) is handled differently: no MixColumns in the last round.
    # For simplicity, we use MixColumns for all rounds and adjust in C++.
    # (The C++ code handles the final round's ShiftRows separately if needed.)

    out.write('/* Forward T-Boxes: g_wb_tbox[round][position][256 entries × 4 bytes] */\n')
    out.write('/* Each entry = MixColumns contribution of SBOX[x ^ round_key[pos]] */\n')
    out.write('/* after ShiftRows moves byte to column (col-row) mod 4 */\n')
    out.write('static const uint8_t g_wb_tbox[14][16][1024] = {\n')

    for r in range(14):
        out.write(f'  /* Round {r} */ {{\n')
        rk = rks[r + 1]  # round key for this round (r=0 uses rk[1] since rk[0] is whitening)
        for pos in range(16):
            out.write(f'    /* Position {pos} */ {{\n')
            col = pos >> 2  # c = pos / 4
            row = pos & 3   # r = pos % 4
            sr_col = (col - row) & 3  # ShiftRows target column

            for x in range(256):
                # Correct Chow-style T-Box:
                # 1. XOR round key byte into input (embedded in SubBytes)
                xored = x ^ rk[pos]
                # 2. SubBytes
                sb = SBOX[xored]
                # 3. Build column input: only this byte's row is set
                col_in = [0, 0, 0, 0]
                col_in[row] = sb
                # 4. MixColumns
                mc = mix_columns_column(col_in)
                # Output: 4 bytes = contribution to column sr_col
                if x < 256:
                    out.write(f'      0x{mc[0]:02x},0x{mc[1]:02x},0x{mc[2]:02x},0x{mc[3]:02x}')
                    if x < 255:
                        out.write(',')
                    if (x + 1) % 4 == 0 and x < 255:
                        out.write('\n')
            out.write('    },\n')
        out.write('  },\n')
    out.write('};\n\n')

    # ---- Checksums from actual table data ----
    out.write('/* Anti-DFA checksums (XOR of all T-Box output bytes per round) */\n')
    out.write('static const uint8_t g_wb_checksum[14] = {\n')
    for r in range(14):
        cksum = 0
        rk = rks[r + 1]
        for pos in range(16):
            col = pos >> 2
            row = pos & 3
            sr_col = (col - row) & 3
            for x in range(256):
                xored = x ^ rk[pos]
                sb = SBOX[xored]
                col_in = [0, 0, 0, 0]
                col_in[row] = sb
                mc = mix_columns_column(col_in)
                cksum ^= mc[0] ^ mc[1] ^ mc[2] ^ mc[3]
        out.write(f'    0x{cksum:02x},\n')
    out.write('};\n\n')

    # ---- Inverse T-Boxes (decrypt) ----
    # InvT-Box: InvSubBytes(x ^ inv_round_key[pos]) → InvShiftRows → InvMixColumns
    # For inverse, we need the inverse round keys (last round's key is first)
    # The round key order for decryption: rk[14], rk[13], ..., rk[1]
    inv_rks = list(reversed(rks[1:]))  # 14 inverse round keys

    out.write('/* Inverse T-Boxes: g_wb_inv_tbox[round][position][256 entries × 4 bytes] */\n')
    out.write('/* Each entry = InvMixColumns contribution of INV_SBOX[x ^ inv_round_key[pos]] */\n')
    out.write('/* after InvShiftRows moves byte to column (col+row) mod 4 */\n')
    out.write('static const uint8_t g_wb_inv_tbox[14][16][1024] = {\n')

    for r in range(14):
        out.write(f'  /* Inverse round {r} */ {{\n')
        rk = inv_rks[r]  # inverse round key
        for pos in range(16):
            out.write(f'    /* Position {pos} */ {{\n')
            col = pos >> 2
            row = pos & 3
            isr_col = (col + row) & 3  # InvShiftRows target column

            for x in range(256):
                # InvSubBytes with embedded round key
                xored = x ^ rk[pos]
                isb = INV_SBOX[xored]
                col_in = [0, 0, 0, 0]
                col_in[row] = isb
                # InvMixColumns
                imc = inv_mix_columns_column(col_in)
                if x < 256:
                    out.write(f'      0x{imc[0]:02x},0x{imc[1]:02x},0x{imc[2]:02x},0x{imc[3]:02x}')
                    if x < 255:
                        out.write(',')
                    if (x + 1) % 4 == 0 and x < 255:
                        out.write('\n')
            out.write('    },\n')
        out.write('  },\n')
    out.write('};\n\n')

    # ---- Inverse checksums ----
    out.write('/* Anti-DFA checksums for inverse T-Boxes */\n')
    out.write('static const uint8_t g_wb_inv_checksum[14] = {\n')
    for r in range(14):
        cksum = 0
        rk = inv_rks[r]
        for pos in range(16):
            col = pos >> 2
            row = pos & 3
            isr_col = (col + row) & 3
            for x in range(256):
                xored = x ^ rk[pos]
                isb = INV_SBOX[xored]
                col_in = [0, 0, 0, 0]
                col_in[row] = isb
                imc = inv_mix_columns_column(col_in)
                cksum ^= imc[0] ^ imc[1] ^ imc[2] ^ imc[3]
        out.write(f'    0x{cksum:02x},\n')
    out.write('};\n\n')

    out.write('/* End of generated tables */\n')


def main():
    parser = argparse.ArgumentParser(description='Generate WB-AES tables (corrected)')
    parser.add_argument('--key', required=True,
                        help='Key: hex:<hex> or "random"')
    parser.add_argument('--output', '-o', default=None,
                        help='Output file (default: stdout)')
    args = parser.parse_args()

    if args.key == 'random':
        key = os.urandom(32)
        print(f'// Generated key: {key.hex()}', file=sys.stderr)
    elif args.key.startswith('hex:'):
        key = bytes.fromhex(args.key[4:])
        if len(key) != 32:
            print('Error: key must be 32 bytes (64 hex chars)', file=sys.stderr)
            sys.exit(1)
    else:
        key = sha256(args.key.encode()).digest()

    if args.output:
        with open(args.output, 'w') as f:
            generate_wb_tables(key, f)
        print(f'Tables written to {args.output}', file=sys.stderr)
    else:
        generate_wb_tables(key)


if __name__ == '__main__':
    main()
