"""
White-Box AES-256 Table Generator for LianYu — CORRECTED

Generates C header with pre-computed lookup tables embedding
the AES-256 key. Run at build time to produce wb_tables.inc.

Usage:
    python3 tools/gen_wb_tables.py --key hex:0123456789abcdef... > wb_tables.inc
    python3 tools/gen_wb_tables.py --key random > wb_tables.inc

Architecture (Chow-style):
  - Round key is embedded INSIDE the T-Box via SBOX[x ^ rk]
  - No separate AddRoundKey step needed
  - Checksums are computed from actual table data
  - Inverse T-Boxes use InvSubBytes embedded inside
"""

